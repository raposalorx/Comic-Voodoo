#include "exception.h"

// --------------------------------------------------------------------------------
//  Ctor+dtor
// --------------------------------------------------------------------------------

Exception::Exception(const std::string& what_str) throw():
  what_str(what_str)
{
}

Exception::~Exception() throw()
{
}


// --------------------------------------------------------------------------------
//  What
// --------------------------------------------------------------------------------

const char* Exception::what() const throw()
{
  return what_str.c_str();
}
